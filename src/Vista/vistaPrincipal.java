    public vistaPrincipal(modeloUsuario modeloUsuario) {
        initComponents();
        campoUsuario.setText(modeloUsuario.getNombre());
        mu = modeloUsuario; //Asigno el valor actual del usuario que inicio sesi칩n
        String tipoUsuario = "";
        switch(modeloUsuario.getPuesto()){
        
            case 1:
                tipoUsuario = "dise침ador";
                btnCatalogoUsuarios.setEnabled(false);
                btnCatalogoPuestos.setEnabled(false);
                btnCatalogoSupervisor.setEnabled(false);
            break;
            case 2:
                tipoUsuario = "supervisor";
                btnCatalogoPedidos.setEnabled(false);
                btnCatalogoUsuarios.setEnabled(false);
                btnCatalogoPuestos.setEnabled(false);
            break;
            case 3:
                tipoUsuario = "administrador";
            break;
        }
        campoTipoUsuario.setText(tipoUsuario);
        campoNumEmpleado.setText(String.valueOf(modeloUsuario.getNum_empleado()));
        DateTimeFormatter formateador = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");
        Runnable runnable = new Runnable() {
            @Override
            public void run() {
                while (true) {
                    try {
                        Thread.sleep(500);
                        etiquetaHora.setText(formateador.format(LocalDateTime.now()));
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                }
            }
        };
        Thread hilo = new Thread(runnable);
        hilo.start();
    }

